				<h3>
					<p>Hace algunas pocas décadas, la realidad virtual era solo ficción. ¿Cómo podríamos imaginarnos que un día esta y otras tecnologías serían parte del cotidiano?</p>
					<p>Si bien en la actualidad la realidad virtual no hace parte del día a día de todos, sabemos de su existencia y de la posibilidad de utilizarla a nuestro favor.</p>
					<p>La educación es una de las áreas que menos se ha desarrollado en estas décadas. La manera de enseñar no ha evolucionado tanto como la tecnología lo ha hecho. Sin embargo, esto es algo que puede, y debe, cambiar.</p>
					<p>¿Cómo se puede aplicar la realidad virtual a la educación?</p>
					<p>A través de la tecnología los niños se transportan, y a pesar de lo que muchos dicen, desarrollan su imaginación, creatividad y aprenden. Se divierten en un ambiente innovador y descubren muchas posibilidades.</p>
					<p>¿Acaso los niños no aprenden rápidamente la dinámica de los videojuegos? Desarrollan estrategias sin saber que lo están haciendo y pueden pasar mucho tiempo entretenidos de esta forma.</p>
					<p>¿Cómo entonces la educación tradicional puede competir con la tecnología? Parece una competencia desleal. Por un lado, un mundo lleno de posibilidades y por el otro, algo monótono.</p>
					<p>Sin embargo, la tecnología y la educación no necesitan estar de lados opuestos. Pueden trabajar juntos, con el mismo objetivo y cuando sea así, ¿te imaginas todo lo que se puede ganar?</p>
					<p>Algunos ejemplos son:</p>
					<li>Viajar y explorar sin limites</li>
					<li>Viajar en el tiempo</li>
					<li>Explorar el cuerpo humano</li>
					<li>Generar empatía e involucrar valores</li>
				</h3>
