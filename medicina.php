				<h3>
					<p>La realidad virtual es una tecnología que nació en la segunda mitad del siglo pasado, y que se expandió inicialmente por el mundo del ocio y del entretenimiento, pero que en la actualidad ya ha trascendido a otros campos.</p>
					<p>La cirugía, por ejemplo, ha encontrado un gran aliado en la realidad virtual. En el artículo Entrenamiento en realidad virtual para complementar el entrenamiento estándar en cirujanos practicantes con experiencia laparoscópica previa limitada, de 2013, los autores concluyeron que el entrenamiento de los cirujanos en realidad virtual parecía reducir la duración de la cirugía y mejoraba el desempeño quirúrgico de los cirujanos.</p>
					<p>En 2008 se concedía gran importancia a esta tecnología a la hora de formar a médicos. En el trabajo Realidad virtual y simulación en el entrenamiento de los estudiantes de medicina, de Guillermo Vázquez-Mata, ya se hablaba de la utilidad de la realidad virtual en el entrenamiento de habilidades complejas como la endoscopia, la laparoscopia o la navegación endovascular.</p>
					<p>Años más tarde, en 2015, se publicó Entrenamiento en realidad virtual para mejorar las aptitudes necesarias para la cirugía otorrinolaringológica, en el que se afirmaba que la simulación en realidad virtual se puede considerar una herramienta adicional de aprendizaje para los estudiantes de medicina.</p>
					<p>Con más de 20 años de investigación, la realidad virtual se usa ya en casos de trastornos de ansiedad, para aliviar el dolor en enfermos de cáncer o para reducir la obesidad. La Neurología, la Psiquiatría o la Psicología también se han visto beneficiadas por el desarrollo de la realidad virtual. Esta tecnología se ha aplicado, por ejemplo, para el tratamiento de algunas fobias o para ayudar a personas con alzhéimer, entre otras patologías.</p>
				</h3>
