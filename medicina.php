				<h3>
					<p>Aplicación de la realidad virtual en la clínica</p>
					<p>Un claro ejemplo de cómo se está aplicando la realidad virtual a la práctica clínica, es el reciente proyecto del Hospital Vall d’Hebron de Barcelona. Desarrollado en 2016, esta iniciativa emplea la realidad virtual para el tratamiento del trastorno por déficit de atención con hiperactividad (TDAH). El equipo utiliza entornos hiperrealistas para aumentar la concentración de los pacientes, una mezcla de mindfulness y realidad virtual.</p>
					<p>La Universidad de Valencia también se apoya en la realidad virtual para la creación de iniciativas que ayuden a los pacientes. Bajo el nombre de Savia, se ha creado un proyecto dirigido a niños con autismo. Para ello, han implementado nuevas herramientas de intervención educativa para niños con trastornos del espectro del autismo (TEA). Mediante tres grupos de videojuegos, el niño interactúa con otros, con sus educadores o con sus padres para aprender una serie de habilidades claves para su desarrollo.</p>
					<p>Por su parte, el Hospital Universitario 12 de Octubre ha presentado La Máquina de los Sueños, una sala de realidad virtual creada especialmente para los niños y adolescentes ingresados en el centro hospitalario. Este espacio hará posible que los pacientes más jóvenes viajen a mundos imaginarios o muy lejanos, como el fondo del océano u otras galaxias. De esta manera, se busca animar a los pacientes durante su estancia en el hospital y reducir su estado de ansiedad cuando reciben tratamiento durante su ingreso, favoreciendo así su recuperación.</p>
