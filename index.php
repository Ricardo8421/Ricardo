				<h3><p>Algunas precisiones: realidad virtual, aumentada y mixta</p>
				<p>Es importante saber diferenciar entre los diferentes tipos de realidad que existen para no confundir conceptos.</p>
				<li>La realidad aumentada muestra un lugar real y proyecta en él ciertos elementos virtuales. Suele usar GPS y la cámara de los dispositivos, no necesita accesorios adicionales. Pokémon Go fue una de las aplicaciones que tuvo más éxito con esta tecnología.</li>
				<li>La realidad virtual, como ya mencionamos, es inmersiva, brinda la experiencia completa y parece que “te transporta” totalmente a otros lugares. Para lograrlo necesitas cascos o lentes especiales.</li>
				<li>Por su parte, la realidad mixta junta las dos anteriores. Permite ver el mundo real con elementos virtuales, pero suma los lentes para poder explorar, movernos y ver dichos elementos desde cualquier ángulo, sin necesidad de un teléfono móvil.</li>
				<p>¿Para qué sirve la realidad virtual?</p>
				<p>Si bien aún no tiene un uso masivo, puede emplearse con diversos fines, aquí repasamos algunos:</p>
