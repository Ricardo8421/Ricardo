				<h3><p>La realidad virtual es una simulación computarizada de espacios nuevos. No solo sirve para entretenimiento, también puede emplearse en medicina y educación.</p>
				<p>Hemos pasado de alabar la tecnología 3D a necesitar experiencias cada vez más reales. Aunque la teletransportación aún no es posible, podemos decir que la realidad virtual (VR por sus siglas en inglés) es un paso gigante en ese objetivo.</p>
				<p>¿Qué es y cómo funciona?</p>
				<p>En palabras de Franco Melly, jefe de redacción de PerúSmart, la realidad virtual “es un entorno en el que la tecnología engaña a tus sentidos para que te sientas como si estuvieras en otro lugar”. Es decir, se trata de una simulación computarizada de espacios diversos en los que podemos interactuar y explorar tal como si estuviéramos ahí realmente.</p>
				<p>Para adentrarse en esta tecnología, el usuario necesita colocarse un casco o unas gafas para empezar con la simulación, estos generalmente están conectados a una computadora, consola o al teléfono móvil.</p>
				<p>La experiencia puede ser complementada por guantes, controles, sistemas de sonido y hasta vestimenta tecnológica con sensores que aumentan el efecto de realidad.</p>
				<p>La cantidad de simulaciones vivenciales que permite es muy diversa. Podemos estar ahora mismo en México, pero con ayuda de unos lentes podemos trasladarnos a China y explorar a 360 grados nuevos lugares.</p>
				<p>Para Melly, lo que falta para que la realidad virtual llegue a su punto más alto, es que su uso se masifique, es decir, que la gente quiera usarla, para que así los fabricantes se motiven a mejorar sus modelos.</p>
				</h3>
